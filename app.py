    if input_mode == "Use companies from dataset":
        col_a, col_b = st.columns(2)
        with col_a:
            asset1_label = st.selectbox("Asset 1", labels, index=min(425, len(labels)-1))
        with col_b:
            default_index = min(32, len(labels)-1)
            asset2_label = st.selectbox("Asset 2", labels, index=default_index)

        row1 = company_lookup(asset1_label, df)
        row2 = company_lookup(asset2_label, df)
        guess1 = guess_asset_inputs(row1)
        guess2 = guess_asset_inputs(row2)
