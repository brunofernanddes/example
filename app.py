def show_company_card(row, assumptions=None):
    if row is None:
        st.info("Company details unavailable.")
        return

    st.markdown(f"### {row['name']} ({row['ticker']})")
    st.write(f"**Industry:** {row['industry']}")
    st.write(f"**Exchange:** {row['exchange']}")
    cols = st.columns(4)
    cols[0].metric("E Grade", f"{row['e_grade']} · {row['e_level']}")
    cols[1].metric("S Grade", f"{row['s_grade']} · {row['s_level']}")
    cols[2].metric("G Grade", f"{row['g_grade']} · {row['g_level']}")
    cols[3].metric("Overall ESG", f"{row['total_grade']} · {row['total_level']}")
