def portfolio_stats(w1: float, w2: float, mu1_pct: float, mu2_pct: float, sigma1_pct: float, sigma2_pct: float, rho: float, rf_pct: float, esg1: float, esg2: float) -> dict:
    x1 = float(max(w1, 0.0))
    x2 = float(max(w2, 0.0))
    rf_weight = max(0.0, 1.0 - x1 - x2)

    mu1 = float(mu1_pct) / 100.0
    mu2 = float(mu2_pct) / 100.0
    rf = float(rf_pct) / 100.0
    s1 = float(sigma1_pct) / 100.0
    s2 = float(sigma2_pct) / 100.0
    rho = float(np.clip(rho, -0.999, 0.999))

    expected_return = rf_weight * rf + x1 * mu1 + x2 * mu2
    variance = (x1 ** 2) * (s1 ** 2) + (x2 ** 2) * (s2 ** 2) + 2.0 * x1 * x2 * rho * s1 * s2
    variance = max(float(variance), 0.0)
    risk = float(np.sqrt(variance))
    sharpe = 0.0 if risk <= 1e-12 else (expected_return - rf) / risk
    esg = float(x1 * esg1 + x2 * esg2)

    return {
        "return_pct": expected_return * 100.0,
        "risk_pct": risk * 100.0,
        "sharpe": sharpe,
        "esg": esg,
        "rf_weight": rf_weight,
    }


def optimise_portfolio(mu1_pct: float, mu2_pct: float, sigma1_pct: float, sigma2_pct: float, rho: float, rf_pct: float, esg1: float, esg2: float, risk_tolerance: int, esg_lambda: float) -> dict:
    mu1 = float(mu1_pct) / 100.0
    mu2 = float(mu2_pct) / 100.0
    rf = float(rf_pct) / 100.0
    s1 = float(sigma1_pct) / 100.0
    s2 = float(sigma2_pct) / 100.0
    rho = float(np.clip(rho, -0.999, 0.999))
    gamma = float(11 - int(risk_tolerance))

    grid = np.linspace(0.0, 1.0, 201)
    x1, x2 = np.meshgrid(grid, grid, indexing="ij")
    feasible = (x1 + x2) <= 1.0 + 1e-12

    portfolio_return = rf + x1 * (mu1 - rf) + x2 * (mu2 - rf)
    variance = (x1 ** 2) * (s1 ** 2) + (x2 ** 2) * (s2 ** 2) + 2.0 * x1 * x2 * rho * s1 * s2
    variance = np.maximum(variance, 0.0)
    risk = np.sqrt(variance)
    portfolio_esg = x1 * float(esg1) + x2 * float(esg2)

    excess_return = portfolio_return - rf
    utility = excess_return - 0.5 * gamma * variance + float(esg_lambda) * (portfolio_esg / 100.0)
    utility = np.where(feasible, utility, -1e18)

    benchmark_utility = excess_return - 0.5 * gamma * variance
    benchmark_utility = np.where(feasible, benchmark_utility, -1e18)

    best_idx = np.unravel_index(int(np.argmax(utility)), utility.shape)
    base_idx = np.unravel_index(int(np.argmax(benchmark_utility)), benchmark_utility.shape)

    best = {
        "w1": float(x1[best_idx]),
        "w2": float(x2[best_idx]),
        "rf_weight": float(max(0.0, 1.0 - x1[best_idx] - x2[best_idx])),
        "return_pct": float(portfolio_return[best_idx] * 100.0),
        "risk_pct": float(risk[best_idx] * 100.0),
        "esg": float(portfolio_esg[best_idx]),
        "sharpe": float(0.0 if risk[best_idx] <= 1e-12 else (portfolio_return[best_idx] - rf) / risk[best_idx]),
    }

    benchmark = {
        "w1": float(x1[base_idx]),
        "w2": float(x2[base_idx]),
        "rf_weight": float(max(0.0, 1.0 - x1[base_idx] - x2[base_idx])),
        "return_pct": float(portfolio_return[base_idx] * 100.0),
        "risk_pct": float(risk[base_idx] * 100.0),
        "esg": float(portfolio_esg[base_idx]),
        "sharpe": float(0.0 if risk[base_idx] <= 1e-12 else (portfolio_return[base_idx] - rf) / risk[base_idx]),
    }

    risk_flat = risk[feasible].ravel() * 100.0
    return_flat = portfolio_return[feasible].ravel() * 100.0
    frontier_df = pd.DataFrame({"risk": risk_flat, "return": return_flat})
    frontier_df["risk_bucket"] = frontier_df["risk"].round(4)
    frontier_df = frontier_df.groupby("risk_bucket", as_index=False).agg({"risk": "mean", "return": "max"}).sort_values("risk")
    frontier_df["cummax"] = frontier_df["return"].cummax()
    frontier_df = frontier_df.loc[np.isclose(frontier_df["return"], frontier_df["cummax"]) | (frontier_df["return"] >= frontier_df["cummax"])]

    return {
        "best": best,
        "benchmark": benchmark,
        "frontier_risk": frontier_df["risk"].to_numpy(dtype=float),
        "frontier_return": frontier_df["return"].to_numpy(dtype=float),
        "rf_pct": rf * 100.0,
        "gamma": gamma,
        "lambda": float(esg_lambda),
    }


def recommendation_result(priority_label: str, risk_tolerance: int, esg_aspect: str, df: pd.DataFrame) -> dict:
    priority_map = {
        "Balanced return and sustainability": "1",
        "Prioritise financial growth": "2",
        "Prioritise sustainability": "3",
    }
    level = risk_level_from_score(risk_tolerance)
    asset1, asset2 = RECOMMENDATIONS[priority_map[priority_label]][level][esg_aspect]

    row1 = company_lookup(asset1, df)
    row2 = company_lookup(asset2, df)
    assumptions1 = ASSET_ASSUMPTIONS.get(asset1, {"expected_return": 8.0, "std_dev": 20.0})
    assumptions2 = ASSET_ASSUMPTIONS.get(asset2, {"expected_return": 8.0, "std_dev": 20.0})
    esg1 = float(0.0 if row1 is None else row1["esg_percentile"])
    esg2 = float(0.0 if row2 is None else row2["esg_percentile"])

    portfolio = portfolio_stats(
        0.5,
        0.5,
        assumptions1["expected_return"],
        assumptions2["expected_return"],
        assumptions1["std_dev"],
        assumptions2["std_dev"],
        0.30,
        4.84,
        esg1,
        esg2,
    )

    return {
        "risk_level": level,
        "asset1": asset1,
        "asset2": asset2,
        "company1": row1,
        "company2": row2,
        "assumption1": assumptions1,
        "assumption2": assumptions2,
        "portfolio": portfolio,
    }


