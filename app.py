def render_company_explorer(df: pd.DataFrame) -> None:
    st.title("Company Explorer")
    query = st.text_input("Search by company, ticker, industry, or exchange", "")

    working = df.copy()
    if query.strip():
        q = query.strip().lower()
        mask = (
            working["name"].str.lower().str.contains(q)
            | working["ticker"].str.lower().str.contains(q)
            | working["industry"].str.lower().str.contains(q)
            | working["exchange"].str.lower().str.contains(q)
        )
        working = working.loc[mask]

    st.write(f"Showing {len(working):,} result(s).")
    display_cols = ["label", "industry", "exchange", "e_grade", "s_grade", "g_grade", "total_grade", "esg_percentile"]
    st.dataframe(
        working[display_cols].rename(columns={
            "label": "Company",
            "industry": "Industry",
            "exchange": "Exchange",
            "e_grade": "E",
            "s_grade": "S",
            "g_grade": "G",
            "total_grade": "Total",
            "esg_percentile": "Normalised ESG",
        }),
        use_container_width=True,
        hide_index=True,
    )


