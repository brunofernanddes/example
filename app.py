def render_builder_page(df: pd.DataFrame) -> None:
    st.title("Portfolio Builder")
    st.write("Build a custom two-asset portfolio and compare the standard optimum with an ESG-tilted optimum.")

    input_mode = st.radio("Asset input mode", ["Use companies from dataset", "Enter assets manually"], horizontal=True)

