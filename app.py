def get_company_matches(query: str, limit: int = 40) -> list[str]:
    query = (query or "").strip().lower()
    options = []
    for record in ESG_COMPANY_DATA:
        searchable = f"{record['name']} {record['ticker']} {record['industry']}".lower()
        if not query or query in searchable:
            options.append(build_company_option(record))
    return options[:limit]


